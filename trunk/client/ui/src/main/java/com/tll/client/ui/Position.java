package com.tll.client.ui;

/**
 * Position - Simple positioning directive used in conjunction with a target
 * ui element.
 * @author jpk
 */
public enum Position {
	/**
	 * {@link #CENTER}: Position in the center of the associated ui element.
	 */
	CENTER,

	/**
	 * {@link #BOTTOM}: Position beneath and left aligned of the associated ui
	 * element.
	 */
	BOTTOM;
}