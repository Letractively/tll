import com.tll.dao.hibernate.TimeStampEntityDao;
import com.tll.dao.impl.ISiteCodeDao;
import com.tll.model.impl.SiteCode;

/**
 * SiteCodeDao
 * @author jpk
 */
public class SiteCodeDao extends TimeStampEntityDao<SiteCode> implements ISiteCodeDao {

	/**
	 * Constructor
	 * @param emPrvdr
	 * @param dbDialectHandler
	 * @param comparatorTranslator
	 */
	@Inject
	public SiteCodeDao(Provider<EntityManager> emPrvdr, IDbDialectHandler dbDialectHandler,
			IComparatorTranslator<Criterion> comparatorTranslator) {
		super(emPrvdr, dbDialectHandler, comparatorTranslator);
	}

	@Override
	public Class<SiteCode> getEntityClass() {
		return SiteCode.class;
	}
}
